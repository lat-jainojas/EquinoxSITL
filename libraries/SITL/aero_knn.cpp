typedef struct { double dist; int idx; } Neighbor;

static inline double distN(const double *a, const double *b) {
    double s = 0.0;
    for (int i = 0; i < N_FEATURES; ++i) {
        double d = a[i] - b[i];
        s += d * d;
    }
    return sqrt(s);
}

static void consider_neighbor(Neighbor nn[], int *filled, int *worst, double dist, int idx) {
    if (*filled < K_SEARCH) {
        nn[*filled].dist =dist;
        nn[*filled].idx = idx; 
        (*filled)++;
        *worst = 0;
        for (int i = 1; i < *filled; ++i)
            if (nn[i].dist > nn[*worst].dist) *worst = i;
    } 
    
    else if(dist < nn[*worst].dist) {
        nn[*worst].dist = dist; nn[*worst].idx = idx;
        *worst = 0;
        for (int i = 1; i < K_SEARCH; ++i)
            if (nn[i].dist > nn[*worst].dist) *worst = i;
    }
}

static int cmp_neighbor(const void* a, const void* b) {
    double da = ((const Neighbor*)a)->dist;
    double db = ((const Neighbor*)b)->dist;
    return (da < db) ? -1 : (da > db);
}

static void predict_internal(const double q[6], double coeffs[6], int *k_out, double *metric_out,   int flight_phase) {
    Neighbor nn[K_SEARCH];
    int filled = 0, worst = 0;
    int num_samples=0;

    for (int i = 0; i < K_SEARCH; ++i) { nn[i].dist = DBL_MAX; nn[i].idx = -1; }
    for (int j = 0; j < N_OUTPUTS; ++j) coeffs[j] = 0.0;

    // Gather K_SEARCH closest
    if (flight_phase ==0)
    {
        num_samples = N_SAMPLES_TAKEOFF;
    }
    else 
    {
        num_samples = N_SAMPLES_CRUISE;
    }
    for (int i = 0; i < num_samples; ++i) 
    {
        double d;

        if (flight_phase==0)
        {
           d = distN(q, X_DATA_TAKEOFF[i]);
        }
        else if(flight_phase ==1)
        {
           d = distN(q, X_DATA_CRUISE[i]);
        }
        else{
            continue;
        }

        consider_neighbor(nn, &filled, &worst, d, i);
    }

    if (filled == 0) { if (k_out) *k_out = 0; if (metric_out) *metric_out = 0.0; return; }

    // Sort ascending by distance
    qsort(nn, filled, sizeof(Neighbor), cmp_neighbor);

    // Exact match? average exact neighbors and return
    int zero_count = 0;
    for (int i = 0; i < filled; ++i) if (nn[i].dist <=1e-12) zero_count++;
    if (zero_count > 0) {
        for (int i = 0; i < filled; ++i) {
            if (nn[i].dist<=1e-12) {

                const double *y = NULL;
                if (flight_phase == 0)
                {
                    y = Y_DATA_TAKEOFF[nn[i].idx];
                }
                else if(flight_phase == 1)
                {
                    y = Y_DATA_CRUISE[nn[i].idx];
                }
        
                for (int j = 0; j < N_OUTPUTS; ++j) coeffs[j] += y[j];
            }
        }
        for (int j = 0; j < N_OUTPUTS; ++j) coeffs[j] /= (double)zero_count;
        if (k_out) *k_out = zero_count;
        // metric_out: for exact matches, report d1 (second-neighbor distance) for reference
        if (metric_out) *metric_out = (filled > 1) ? nn[1].dist : nn[0].dist;
        return;
    }

    // ---- Adaptive metric & k selection ----
    double metric;
#ifdef ADAPT_USE_RATIO
    // ratio r = d_k / d_1
    double d1 = (filled > 1) ? nn[1].dist : nn[0].dist;
    int kidx  = (filled >= K_FOR_GAP) ? (K_FOR_GAP - 1) : (filled - 1);
    if (kidx < 1) kidx = 1; // ensure we don't use the 0th (self) or underflow
    double dk = nn[kidx].dist;
    metric = dk / (d1 > 0.0 ? d1 : EPS_DIST);
    int k = (metric > RATIO_SPLIT) ? K_SPARSE : K_DENSE;
#else
    // legacy: d1 trigger
    metric = (filled > 1) ? nn[1].dist : nn[0].dist;
    int k = (metric > NN_GAP_SPLIT) ? K_SPARSE : K_DENSE;
#endif
    if (k > filled) k = filled;

    // Distance-weighted average on chosen k
    double wsum = 0.0;
    for (int i = 0; i < k; ++i) {
        double d = nn[i].dist;
        if (d <= 0.0) d = EPS_DIST;
        double w = 1.0 / d;
        const double *y = NULL;
        if (flight_phase == 0)
        {
             y = Y_DATA_TAKEOFF[nn[i].idx];
        }
        else if(flight_phase == 1)
        {
             y = Y_DATA_CRUISE[nn[i].idx];
        }

        for (int j = 0; j < N_OUTPUTS; ++j) coeffs[j] += w * y[j];
        wsum += w;
    }
    if (wsum > 0.0) {
        for (int j = 0; j < N_OUTPUTS; ++j) coeffs[j] /= wsum;
    }

    if (k_out) *k_out = k;
    if (metric_out) *metric_out = metric; // ratio (or d1 in legacy build)
}

void aero_interpolate(double throttle, double aileron, double elevator,
                      double rudder, double beta, double aoa,
                      double coeffs[6],int flight_phase) {
    double q[6] = { throttle, aileron, elevator, rudder, beta, aoa };
    predict_internal(q, coeffs, NULL, NULL, flight_phase);
}

void aero_interpolate_ex(double throttle, double aileron, double elevator,
                         double rudder, double beta, double aoa,
                         double coeffs[6], int *chosen_k, double *nn_gap /* metric */,int flight_phase) {
    double q[6] = { throttle, aileron, elevator, rudder, beta, aoa };
    predict_internal(q, coeffs, chosen_k, nn_gap, flight_phase);
}


